    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String marca = campoMarca.getText();
        String modelo = campoModelo.getText();
        String cilindrada = campoCilindrada.getText();
        VeiculoController controller = new VeiculoController();
        
        if(controller.cadastrarMoto(marca, modelo, cilindrada)){
            JOptionPane.showMessageDialog(null, "Moto Cadastrado com sucesso", "Cadastrar",
                                         JOptionPane.INFORMATION_MESSAGE);
            tela.adicionarNaTabela(marca, modelo, "Moto");
            tela.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

